def search_prisoners(query):
    """
    Mencari buronan berdasarkan nama, alias, kejahatan, lokasi terakhir terlihat,
    atau deskripsi.
    """
    results = []
    query = query.lower()  # Pencarian tidak peka huruf besar/kecil
    for prisoner in prisoners:
        if any(query in str(value).lower() for value in prisoner.values()):
            results.append(prisoner)
    return results

def show_results(results):
    if results:
        print("\n=== Profil Buronan Ditemukan ===")
        for prisoner in results:
            print(f"""
Nama: {prisoner['name']}
Alias: {prisoner['alias']}
Kejahatan: {prisoner['crime']}
Lokasi Terakhir: {prisoner['last_seen']}
Deskripsi: {prisoner['description']}
Hadiah: {prisoner['bounty']}
""")
    else:
        print("\nTidak ada buronan yang sesuai dengan pencarian Anda.")

def main():
    while True:
        print("\n=== Sistem Pencarian Buronan ===")
        query = input("Masukkan nama, alias, kejahatan, atau informasi lain (ketik 'keluar' untuk berhenti): ")
        if query.lower() == "keluar":
            print("Terima kasih telah menggunakan sistem pencarian ini!")
            break
        results = search_prisoners(query)
        show_results(results)

if __name__ == "__main__":
    main()
